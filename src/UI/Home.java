    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        
        String username = uname.getText();
        String pass = new String(password.getPassword());
        
        if (!user.isSelected() & !admin.isSelected()) {
            JOptionPane.showMessageDialog(null, "Please selece account type");
        } else {
            if (admin.isSelected()) {
                AdminController ac = new AdminController();
                Admin a = new Admin();
                a.setUserName(username);
                a.setPassword(pass);

                boolean loginStatus = ac.checkLogin(a);

                if (loginStatus) {
                    mainPanel.removeAll();
                    mainPanel.add(dashboardAdmin);
                    mainPanel.repaint();
                    mainPanel.revalidate();
                    
                    ResultSet res = ac.getUserInfo(username, pass);
                    
                    try {
                        lblUserName1.setText("Welcome: "+res.getString(2) + " " + res.getString(3));
                    } catch (SQLException ex) {
                        System.out.println(ex.getMessage());
                    }
                    
                    time(lblTime1);
                    
                } else {
                    jblInfo.setVisible(true);
                }  
            } else if (user.isSelected()) {
                UserController uc = new UserController();
                User u = new User();
                u.setUserName(username);
                u.setPassword(pass);

                boolean loginStatus = uc.checkLogin(u);

                if (loginStatus) {
                    mainPanel.removeAll();
                    mainPanel.add(dashboardUser);
                    mainPanel.repaint();
                    mainPanel.revalidate();
                    
                    ResultSet res = uc.getUserInfo(username, pass);
                    
                    try {
                        jblUserName.setText("Welcome: "+res.getString(2) + " " + res.getString(3));
                    } catch (SQLException ex) {
                        System.out.println(ex.getMessage());
                    }
                    
                    time(lblTime);
                    
                } else {
                    jblInfo.setVisible(true);
                }
            }
        }         
        
    }//GEN-LAST:event_btnLoginActionPerformed

