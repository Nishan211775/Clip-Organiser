    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String clipName = jTextField2.getText();
        int min = (Integer) jSpinner7.getValue();
        int sec = (Integer) jSpinner11.getValue();
        String clipType = jComboBox4.getSelectedItem().toString();
        boolean advertisement = false;
        
        if (jCheckBox2.isSelected()) {
            advertisement = true;
        } else if(jCheckBox1.isSelected()) {
            advertisement = false;
        }
        
        int noOfTimesItsBeenPlayedAccrossTheStation = (Integer) jSpinner10.getValue();
        int maxNoOfTimesItCanPlayedInCurrentPeriod = (Integer) jSpinner8.getValue();
        int minNoOfTimesItCanPlayedInCurrentPeriod = (Integer) jSpinner9.getValue();
        String genre = jComboBox5.getSelectedItem().toString();
        
        
        if(clipName.equals("") || min == 0 || sec == 0 || clipType.equals("") || 
                noOfTimesItsBeenPlayedAccrossTheStation == 0 || maxNoOfTimesItCanPlayedInCurrentPeriod == 0 ||
                minNoOfTimesItCanPlayedInCurrentPeriod == 0) {
            
            JOptionPane.showMessageDialog(null, "Please fill up all the field properly");
        } else {
            String duration = "00:"+min+":"+sec;
            Time clipDuration = Time.valueOf(duration);
            
            Clips c = new Clips();
            c.setClipName(clipName);
            c.setClipDuration(clipDuration);
            c.setClipType(clipType);
            c.setAdvertisement(advertisement);
            c.setTimesPlayedAccrossStation(noOfTimesItsBeenPlayedAccrossTheStation);
            c.setMaxTimeCanPlayedInCurrentStation(maxNoOfTimesItCanPlayedInCurrentPeriod);
            c.setMinTimeMustPlayedInCurrentStation(minNoOfTimesItCanPlayedInCurrentPeriod);
            c.setGenre(genre);
            
            ClipsController cc = new Controller.ClipsController();
            int res = cc.addClips(c);
            
            if (res > 0) {
                JOptionPane.showMessageDialog(null, "Clips Saved");
                
                jTextField2.setText("");
                jSpinner7.setValue(0);
                jSpinner11.setValue(0);
                jSpinner10.setValue(0);
                jSpinner8.setValue(0);
                jSpinner9.setValue(0);
                
            } else {
                JOptionPane.showMessageDialog(null, "Unable to save clip, please try again");
            }
        }
    }//GEN-LAST:event_jButton6ActionPerformed

