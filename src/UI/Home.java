    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        jButton4.setFocusPainted(false);
        String fname = lblFname.getText();
        String lname = lblLname.getText();
        String uname = lblUname.getText();
        String pass = new String(lblPassword.getPassword());
        String repass = new String(lblRepass.getPassword());
        String accountType = "";
        
        if (rbAdmin.isSelected()) {
            accountType = "admin";
        } else if(rbUser.isSelected()) {
            accountType = "user";
        }
        
        if (accountType.equals("")) {
            JOptionPane.showMessageDialog(null, "Please select account type");
        } else {
            if (lname.equals("") | fname.equals("") | uname.equals("") | pass.equals("") | repass.equals("")) {
                JOptionPane.showMessageDialog(null, "Please fill all the required field.");
            } else {
                if (!pass.equals(repass)) {
                    lblInfo.setVisible(true);
                } else {
                    
                    if (rbAdmin.isSelected()) {
                        AdminController ac = new AdminController();
                        Admin a = new Admin();

                        a.setFristName(fname);
                        a.setLastName(lname);
                        a.setUserName(uname);
                        a.setPassword(pass);


                        int res = ac.createAccount(a);

                        if (res > 0) {
                            JOptionPane.showMessageDialog(null, "Saved");
                            
                            mainPanel.removeAll();
                            mainPanel.add(loginPanel);
                            mainPanel.repaint();
                            mainPanel.revalidate();
                            
                        } else {
                            JOptionPane.showMessageDialog(null, "Unable to save");
                        }
                        
                    } else if (rbUser.isSelected()) {
                        UserController uc = new UserController();
                        User u = new User();

                        u.setFristName(fname);
                        u.setLastName(lname);
                        u.setUserName(uname);
                        u.setPassword(pass);


                        int res = uc.createAccount(u);

                        if (res > 0) {
                            JOptionPane.showMessageDialog(null, "Saved");
                            
                            mainPanel.removeAll();
                            mainPanel.add(loginPanel);
                            mainPanel.repaint();
                            mainPanel.revalidate();
        
                        } else {
                            JOptionPane.showMessageDialog(null, "Unable to save");
                        }
                    }
                }
            }
        }
               
    }//GEN-LAST:event_jButton4ActionPerformed

