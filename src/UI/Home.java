    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        final String showName = lblShowName.getText();
        final Time showDuration = getTime(lblDuration);
        final Time audioDuration = getTime(jSpinner5);
        final Time videoDuration = getTime(jSpinner2);
        final Time advertisingDuration = getTime(jSpinner3);
        final Time voiceoverDuration = getTime(jSpinner4);
        final String audioOrMultimedia = jComboBox2.getSelectedItem().toString();
        final int showsRunsPerDay = (Integer) jSpinner6.getValue();
        final String genre = jComboBox3.getSelectedItem().toString();
        
        if (showName.equals("")) {
            JOptionPane.showMessageDialog(null, "Please enter show name");
        } else {
            Model.Shows s = new Model.Shows();
            s.setShowName(showName);
            s.setShowDuration(showDuration);
            s.setAudioDuration(audioDuration);
            s.setVideoDuration(videoDuration);
            s.setAdvertisingDuration(advertisingDuration);
            s.setVoiceoverDuration(voiceoverDuration);
            s.setAudioOrMultimedia(audioOrMultimedia);
            s.setShowsRunsPerDay(showsRunsPerDay);
            s.setGenre(genre);
            
            final int res = new Controller.ShowsController().addShows(s);
            
            if (res > 0) {
                JOptionPane.showMessageDialog(null, "Shows saved");
            }
             
        }
    }//GEN-LAST:event_jButton7ActionPerformed

