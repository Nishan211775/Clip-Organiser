    private Time getTime(JSpinner name) {
        Date time = (Date) name.getValue();
        SimpleDateFormat sdf = new SimpleDateFormat("HH:mm:ss");
        String date = sdf.format(time);
        Time showDuration = Time.valueOf(date);
        
        return showDuration;
    }
    
